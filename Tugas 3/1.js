// 10 contoh built-in function di javascript

const array = [1, 3, 5, 7];
//concat() : Untuk menggabungkan array
console.log(array.concat(2, 4, 6, 8));

//length() : Untuk menghitung total indeks pada sebuah array
console.log(array.length);

const string = 'HeLlO WorlD'
//toLowerCase() : Untuk mengubah string menjadi lower case 
console.log(string.toLowerCase());

//toUpperCase() : Untuk mengubah string menjadi kapital
console.log(string.toUpperCase());

//Math.abs. Untuk mendapatkan nilai absolut dari suatu angka
console.log(Math.abs(-11));

//Math.max. Untuk mendapatkan nilai terbesar dari suatu array yang berisi angka
console.log(Math.max(1,2,3));

//Math.min. Untuk mendapatkan nilai terkecil dari suatu array yang berisi angka
console.log(Math.min(1,2,3));

//Math.round Untuk membulatkan floating number
console.log(Math.round(2.3));

//Math.sqrt untuk mendapatkan niai akar kuadrat dari suatu bilangan
console.log(Math.sqrt(16));

//Data. Untuk mendapatkan tanggal dan waktu sesuai local time
console.log(Date());
